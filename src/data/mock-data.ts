export const prismSphere: ModelItem = {
  id: "root",
  name: "Prism Sphere",
  type: "group",

  expanded: true,
  children: (() => {
    const t = (1 + Math.sqrt(5)) / 2;

    const baseVerts = [
      [-1, t, 0],
      [1, t, 0],
      [-1, -t, 0],
      [1, -t, 0],
      [0, -1, t],
      [0, 1, t],
      [0, -1, -t],
      [0, 1, -t],
      [t, 0, -1],
      [t, 0, 1],
      [-t, 0, -1],
      [-t, 0, 1],
    ];

    const normalize = ([x, y, z]: number[]) => {
      const len = Math.hypot(x, y, z);
      return [x / len, y / len, z / len];
    };

    const unitVerts = baseVerts.map(normalize);

    const layers = [1.5, 2.0, 2.5, 3.0, 3.5];
    const allVerts: [number, number, number][] = [];

    layers.forEach((radius) => {
      unitVerts.forEach((v) => {
        allVerts.push(v.map((c) => c * radius) as [number, number, number]);
      });
    });

    const colors = [
      "#FF2E63",
      "#08F7FE",
      "#7CFF6B",
      "#FFBE0B",
      "#9B59B6",
      "#00D9FF",
      "#FF6B6B",
      "#1FAB89",
      "#F46036",
      "#6A4C93",
      "#FFD23F",
      "#E63946",
    ];

    return allVerts.map((pos, i) => ({
      id: `prism-${i}`,
      name: `Prism ${i + 1}`,
      type: "box" as const,
      position: pos,
      rotation: [0, 0, 0],
      scale: [0.2, 0.2, 0.2],
      color: colors[i % colors.length],
      dimensions: { Width: 1, Height: 1, Depth: 1 },
      children: [],
    }));
  })(),
};
